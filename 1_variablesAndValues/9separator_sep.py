# sep='' called separator to specify what to separate args
print(2,3,4)
print(2,3,4, sep='---')

print('a','b','c')
print('a','b','c', sep='__')


w, x, y, z = 10, 15, 20, 25 
print(w, x, y, z) 
print(w, x, y, z, sep=',')
print(w, x, y, z, sep='') 
print(w, x, y, z, sep=':') 
print(w, x, y, z, sep='-----')